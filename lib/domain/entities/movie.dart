class Movie {
  final String id;
  final String name;
  final String releaseDate;
  final String genre;
  final String duration;
  final String imageUrl;
  final String fullDescription;

  Movie({
    required this.id,
    required this.name,
    required this.releaseDate,
    required this.genre,
    required this.duration,
    required this.imageUrl,
    required this.fullDescription,
  });
}
